//! # 工作流异常处理模块 (Error Handling)
//!
//! 本模块定义了系统底层的全局领域级错误类型。
//! 借助 `thiserror` 库提供了强类型的错误抽象与上下文追踪能力，确保在
//! 复杂的 DAG 拓扑解析与并发异步执行环境中，所有异常均能被精准捕获、
//! 安全转换并向上传递至调用栈或前端视图。

// src/error.rs
use thiserror::Error;

/// 工作流执行引擎的全局领域级错误枚举。
///
/// 实现了标准的 `std::error::Error` 契约，支持底层异常（如反序列化失败）
/// 向领域级错误的自动隐式转换。这保证了在庞大的有向无环图 (DAG) 调度中，
/// 依然能够提供清晰、可溯源的错误堆栈。
#[derive(Error, Debug)]
pub enum WorkflowError {
    /// 触发于有向无环图 (DAG) 拓扑构建与校验阶段。
    ///
    /// 当系统在解析 YAML 拓扑图时，检测到节点间存在循环依赖（如 A -> B -> A）时抛出。
    /// 工作流引擎强制要求图谱必须严格无环，以防止并发调度器在运行时陷入死循环。
    #[error("☠️ 构建失败：检测到配置的工作流中存在死循环(环路)，不符合 DAG 要求！")]
    CycleDetected,

    /// 触发于工作流配置文件反序列化阶段。
    ///
    /// 自动拦截并透明包装来自 `serde_yaml` 库的底层语法解析错误
    /// （如缩进错误、字段类型不匹配或非法字符）。
    #[error("📄 YAML 配置文件解析失败: {0}")]
    ParseYamlError(#[from] serde_yaml::Error),

    /// 触发于节点工厂的反射与实例化阶段。
    ///
    /// 当配置文件中声明了引擎字典中尚未注册、或拼写错误的 `node_type` 标识符时抛出。
    #[error("❓ 未知的节点类型: '{0}'")]
    UnknownNodeType(String),

    /// 触发于底层调度器的运行时节点计算阶段。
    ///
    /// 该变体不仅记录了错误信息，还强绑定了触发异常的具体节点 ID (`node_id`)，
    /// 以便于在终端界面 (TUI) 或日志输出中进行精确的故障隔离与渲染。
    #[allow(dead_code)] // 预留作为运行时的细粒度错误载体
    #[error("❌ 节点执行失败 [{node_id}]: {reason}")]
    NodeExecutionFailed {
        /// 发生崩溃的组件节点全局唯一标识符。
        node_id: String,
        /// 底层抛出的具体异常原因文本。
        reason: String,
    },
}
